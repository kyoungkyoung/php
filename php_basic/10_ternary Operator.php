<?php
//Ternary Operator ; 삼항 연산자 -> 조건문

// 조건식이 true면 a값을, false면 b값을 출력
// 조건식 ? true(a) : false(b);
echo true ? 'Hello, world' : 'Bye'; // Hello, world
echo false ? 'Hello, world' : 'Bye'; // Bye

// 조건문을 더 간단히 쓴것!!
// A ?: B -> A가 true면 A값을, A가 false면 B값을 출력
echo 'Hello, world' ?: 'Bye'; // Hello, world
echo false ?: 'Bye'; // Bye
